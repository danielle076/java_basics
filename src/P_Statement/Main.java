package P_Statement;

public class Main {

    public static void main(String[] args) {
        int myVariable = 50;
        // 'myVariable = 50' is an expression
        // 'int myVariable = 50;' is a statement

        // a semicolon is needed to complete a Java line to make it a statement
        myVariable++;
        myVariable--;
        System.out.println("This is a test");
    }
}
